export type I18nKey = keyof typeof translations.en;

export function tr(language: Language, key: I18nKey): string {
  return translations[language][key] ?? translations.en[key];
}

// 以中文原文为 key 的翻译：zh 直接返回原文，en 查字典、缺失回退中文。
export function translate(zh: string, language: Language): string {
  if (language === 'zh') return zh;
  return zhEn[zh] ?? zh;
}
